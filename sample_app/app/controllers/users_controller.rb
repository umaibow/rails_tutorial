class UsersController < ApplicationController
	def new
		@user = User.new
	end

	def show
		@user = User.find(params[:id])
	end
	
	def create
		@user = User.new(user_params)
		if @user.save
			#保存成功
			#ログイン
			login_@user
			#成功時のメッセージ表示
			flash[:success] = "Welcome to the Sample App!"
			#ユーザーページへリダイレクト
			redirect_to @user
		else
			#保存失敗
			render 'new'
		end
	end
	
	private
	def user_params
		params.require(:user).permit(:name, :email, :password, :password_confirmation)
	end
end
